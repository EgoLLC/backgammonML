data class Input(
    val p1Checker2Position: Array<FloatArray>,
    val p2Checker2Position: Array<FloatArray>,
    val movesVariants: Array<FloatArray>,
) {
    override fun toString(): String {
        val p1Checker = p1Checker2Position.joinToString("") { "${it.map { "$it" }}\n            " }
        val p2Checker = p2Checker2Position.joinToString("") { "${it.map { "$it" }}\n            " }
        val moves = movesVariants.joinToString("") { "${it.map { "$it" }}\n            " }
        return "p1Checker = ${p1Checker}" +
                "\np2Checker = ${p2Checker}" +
                "\nmoves =     ${moves}"

    }

    fun getFullBoard() = arrayOf(
        p1Checker2Position,
        p2Checker2Position,
        movesVariants
    ).flattenFloats()

    fun getMoves() = arrayOf(
        movesVariants
    ).flattenFloats()

}

